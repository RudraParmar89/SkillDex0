// Paddle Server-Side API Utilities
// Use this in your backend/Edge Functions

const PADDLE_API_URL = 'https://api.paddle.com/v1';

const headers = {
  'Content-Type': 'application/json',
  'Authorization': `Bearer ${process.env.PADDLE_API_KEY}`,
};

interface FetchOptions {
  method?: string;
  body?: Record<string, unknown>;
}

async function paddleFetch(endpoint: string, options: FetchOptions = {}) {
  const url = `${PADDLE_API_URL}${endpoint}`;
  
  const response = await fetch(url, {
    method: options.method || 'GET',
    headers,
    ...(options.body && { body: JSON.stringify(options.body) }),
  });

  if (!response.ok) {
    throw new Error(`Paddle API error: ${response.statusText}`);
  }

  return response.json();
}

// Customers
export async function createCustomer(email: string, name?: string) {
  return paddleFetch('/customers', {
    method: 'POST',
    body: {
      email,
      name,
      locale: 'en',
    },
  });
}

export async function getCustomer(customerId: string) {
  return paddleFetch(`/customers/${customerId}`);
}

export async function updateCustomer(customerId: string, data: Record<string, unknown>) {
  return paddleFetch(`/customers/${customerId}`, {
    method: 'PATCH',
    body: data,
  });
}

